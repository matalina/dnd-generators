export function getRandomKeywords(number = 3) {
  const words = [];
  for (let i = 0; i < number; i++) {
    let word = getRandomWord();
    while (words.includes(word)) {
      word = getRandomWord();
    }
    words.push(word)
  }
  return words;
}

function getRandomWord() {
  const d8 = new Dice(8, 1);
  const d100 = new Dice(100, 1);
  let hundred = d8.roll().add();
  if (hundred === 8) hundred = 0;
  const index = hundred * 100 + d100.roll().add();
  return keywords[index];
}